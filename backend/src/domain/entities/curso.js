class Curso {
    constructor(nivel, grado, especialidad) {
      this.nivel = nivel;
      this.grado = grado;
      this.especialidad = especialidad;
    }
  
    setNivel(nivel) {
      this.nivel = nivel;
    }
  
    getNivel() {
      return this.nivel;
    }
  
    setGrado(grado) {
      this.grado = grado;
    }
  
    getGrado() {
      return this.grado;
    }
  
    setEspecialidad(especialidad) {
      this.especialidad = especialidad;
    }
  
    getEspecialidad() {
      return this.especialidad;
    }
  }
  
